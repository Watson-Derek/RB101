#1. != is used for 'not equal to', and is used when you want to make sure two 
#   comparables are not equal

#2. ! before something is a boolean negate, means 'not' something

#3. ! after something on a method usually denotes mutating the caller

#4. ? before something is ternary, for if else

#5. ? after something is a check as to whether the method before it is true for the caller

#6. !! before something turns any object into its boolean equivalent (true/false)